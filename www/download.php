<h2>Downloading CEDET</h2>
</td></tr></table>

<p>CEDET tools including EIEIO, Semantic, Speedbar, EDE, and COGRE are
now distributed together in a single file.  This simplifies
installation and version management.

<p>While Some individual CEDET packages have active stable releases, the
CEDET bundle is currently only available in Beta.  CEDET Betas are
the only place to get the latest versions of individual tools.

<p>Try out
<a href="http://sourceforge.net/project/showfiles.php?group_id=17886&release_id=192965">
cedet-1.0beta1c.tar.gz</a>.

